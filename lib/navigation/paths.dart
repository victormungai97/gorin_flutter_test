part of 'navigation.dart';

/// Class containing paths (routes) representing the various pages in app.
/// These paths are then used in navigation, both internally and when sourced
/// from external sources e.g. URL in browser or deep/app links

class Paths {
  /// Path to the default page
  static const root = '/';

  /// Path to signup page
  static const register = '/register';

  /// Path to signin page
  static const login = '/login';

  /// Path to home page
  static const home = '/home';
}
